    protected const ARCHIVE_SIZE = 3094366568;

    public function run(): void
    {
        $courses = Section::where('slug', 'courses')->value('id');

        $position = 0;
        foreach ($this->pages() as $oldPath => $data) {
            $data['section_id'] = $courses;
            $data['slug'] = Str::slug($data['title']);
            $data['status'] = 'draft';
            $data['source_type'] = 'archive_xintellect';
            $data['source_url'] = 'https://web.archive.org/web/2015/http://www.x-intellect.org'.$oldPath;
            $data['position'] = ++$position;

            $page = Page::updateOrCreate(['slug' => $data['slug']], $data);

            // 301 со старого адреса описания курса
            Redirect::updateOrCreate(
                ['from_path' => rtrim($oldPath, '/')],
                ['to_url' => $page->url(), 'status_code' => 301, 'comment' => 'Курсы: '.Str::limit($page->title, 50)],
            );
        }
    }

